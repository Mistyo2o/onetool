public class DissList {

    private String diss_name;
    private String diss_cover;
    private String song_cnt;
    private String tid;

    public String getDiss_name() {
        return diss_name;
    }

    public void setDiss_name(String diss_name) {
        this.diss_name = diss_name;
    }

    public String getDiss_cover() {
        return diss_cover;
    }

    public void setDiss_cover(String diss_cover) {
        this.diss_cover = diss_cover;
    }

    public String getSong_cnt() {
        return song_cnt;
    }

    public void setSong_cnt(String song_cnt) {
        this.song_cnt = song_cnt;
    }

    public String getTid() {
        return tid;
    }

    public void setTid(String tid) {
        this.tid = tid;
    }
}
